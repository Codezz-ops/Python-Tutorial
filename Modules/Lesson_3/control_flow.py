# Lesson 3: Introduction Python Control Flow
# This script demonstrates Python Control Flow.

fruits = ["apple", "banana", "cherry"]
for fruit in fruits:
    print(fruit)

count = 1
while count <= 5:
    print(count)
    count += 1

age = 18
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")
